    public void MostrarC(){
        String cadena;
        cadena="-----COLA PRIORIDADBICOLA-----\n";
        if(ColaBP.ini==ColaBP.fin && ColaBP.ini==-1){
            cadena+="!!!COLA VACIA!!!";
            jTextArea1.setText(cadena);
            return;
        }
        Data[] cola= ColaBP.getCola();
        for(int i=ColaBP.ini; i<=ColaBP.fin; i++){
            cadena+=" - ["+i+"] "+cola[i].getData();
        }
            jTextArea1.setText(cadena);
    }
    
